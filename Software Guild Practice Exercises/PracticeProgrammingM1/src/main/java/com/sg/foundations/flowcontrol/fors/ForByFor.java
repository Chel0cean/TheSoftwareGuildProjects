
package com.sg.foundations.flowcontrol.fors;

/**
 *
 * @author chelseamiller
 */
public class ForByFor {
    public static void main(String[] args) {

        for (int a = 0; a < 1; a++) {
            //this is the first 3 in each line
            System.out.print("|");

            //the j< sets the number of columns
            for (int b = 0; b < 1; b++) {
                for (int c = 0; c < 3; c++) {
                    System.out.print("*");
                    
                
            }
                System.out.print("|");
                for (int d=0; d<3; d++){
                        System.out.print("$");
                        
                    }
                System.out.print("|");
                for (int e = 0; e < 3; e++) {
                    System.out.print("*");
                
            }
                
                //this is the middle and end
            System.out.print("|");
            }
            
       System.out.println("");
    }  
        for (int f = 0; f < 1; f++) {
            //this is the first 3 in each line
            System.out.print("|");

            //the j< sets the number of columns
            for (int g = 0; g < 1; g++) {
                for (int h = 0; h < 3; h++) {
                    System.out.print("@");
                    
                
            }
                System.out.print("|");
                for (int i=0; i<3; i++){
                        System.out.print("#");
                        
                    }
                System.out.print("|");
                for (int j = 0; j < 3; j++) {
                    System.out.print("@");
                
            }
                
                //this is the middle and end
            System.out.print("|");
            }
            
       System.out.println("");
    } 
        for (int k = 0; k < 1; k++) {
            //this is the first 3 in each line
            System.out.print("|");

            //the j< sets the number of columns
            for (int l = 0; l < 1; l++) {
                for (int m = 0; m < 3; m++) {
                    System.out.print("*");
                    
                
            }
                System.out.print("|");
                for (int n=0; n<3; n++){
                        System.out.print("$");
                        
                    }
                System.out.print("|");
                for (int o = 0; o < 3; o++) {
                    System.out.print("*");
                
            }
                
                //this is the middle and end
            System.out.print("|");
            }
            
       System.out.println("");
    } 
}
}


